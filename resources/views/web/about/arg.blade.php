    <p>
        Um <b>Jogo de Realidade Alternativa</b> (em inglês: <b>alternate reality game</b>, ou <b>ARG</b>) é um tipo de jogo eletrônico que combina as situações de jogo com a realidade, recorrendo às mídias do mundo real, de modo a fornecer aos jogadores uma experiência interativa.<br/>
        Os ARGs envolvem os jogadores nas histórias, encorajando-os a explorar a narrativa, a resolver os desafios e a interagir com as personagens do jogo. Este tipo de jogos desenvolve-se a partir de sites, e-mails, telefonemas, entre outros meios de comunicação comuns.
