    <h3 style="text-align: center">Criando personagens animais antropomórficos e toda a cultura em volta deles!</h3>
    <p>
        A expressão <b>furry</b> (peludo em inglês) é comumente usada para se referir a diferentes conceitos, geralmente relacionados a animais antropomórficos. <br/>
        Alguém que diz ser furry está geralmente expressando um interesse em criaturas, personagens ou animais antropomórficos.<br/>
        As pessoas auto-intituladas furries com frequência são pessoas que participam de uma cultura focada na natureza, sendo amantes de personagens imaginários dos autores de desenhos animados e visualizando lugares onde seres das mais variadas raças mitológicas tomam vida e agem em uma sociedade paralela à humana, porém nem todos os furries batem 100% com essa descrição.<br/>
        Existem vários tipos de furries, tal como os teriantropos aqueles que cultuam valores que remetem ao druidismo e reverência ao espírito da natureza , cartoon furries (que são os genuínos amantes de desenhos animados) ou os Antropomórficos cujos personagens experimentam semelhanças com a sociedade humana nos vários períodos da história, lembrando muitas vezes a Era Vitoriana, mas com a diferença de que os avatares ou fursonas são humanos com características animais, tais como patas, caudas, cabeça (semelhante a seres mitológicos de todas as sociedades antigas, como o Anúbis na cultura egípcia, a sereia ou lobisomens).<br/>
        A sociedade furry também participa e organiza convenções, às vezes com a participação de editoras de histórias em quadrinhos. A cultura furry está presente também em jogos de videogame e computador, como o Inherit The earth, Star Fox, Final Fantasy, entre outros.<br/>
        Personagens furries existem desde quando a humanidade imaginou deuses que são humanos, ao mesmo tempo que são criaturas da natureza, hoje sendo usados principalmente em tiras cômicas nos jornais e desenhos animados.
    </p>

    <h3>O que é Fursona?</h3>
    <p>
        <b>Fursona</b> (ou furry pessoal) é a representação que uma pessoa faz de si mesma através de um personagem furry. É uma espécie de abreviação de <b>furry persona</b> (personagem furry).
    </p>

    <h3>O que é Fursuit?</h3>
    <p>
        <b>Fursuit</b> é o termo em inglês para o traje de pelúcia (ou outros materiais) ou fantasia usada pelos fursuiters (furries que usam tais trajes). Esses trajes são derivados das fantasias de animais usados como mascotes em eventos esportivos.<br/>
        As fursuits também são semelhantes a bonecos live-action de personagens utilizados em eventos infantis, peças de teatro ou produções televisivas. Porém a principal diferença é que os fursuiters "encarnam" o personagem ou fursona, praticando uma forma diferente de roleplay, similar ao live action.<br/>
        Embora seja a parte mais tangível do fandom, nem todos os furries são fursuiters. Uma pesquisa realizada nos EUA mostrou que apenas 18% dos furries possuem uma fursuit ou desejam possuir uma.
    </p>

    <h3>O que é Furmeet?</h3>
    <p>
        Um <b>furmeet</b> (ou <b>furcontro</b>) é um pequeno encontro de furries, geralmente marcados com uma antecedência pequena em um local público ou na casa de um furry. Os furmeets são feitos para que os furries possam ter contato pessoal com outros furries. Furmeet são geralmente feitos em cidades onde há grande quantidade de furries. Alguns furries viajam para outras localidades para participarem de furmeets, aproveitando a ocasião para fazer turismo.<br/>
        Além de fazer novas amizades ou rever contatos antigos, os participantes de furmeets podem aprender mais sobre o fandom através das artes (desenhos, artesanato, fursuits) exibidas por outros furries durante o evento.
    </p>

